package de.teilautos.registration;

import java.io.Serializable;
import java.time.Instant;

public class User implements Serializable {
	private static final long serialVersionUID = 1704654937372532050L;

	private long id;
	private String username;
	private String email;
	private String firstname;
	private String surname;
	private boolean active;
	private Instant lastLogin;
	private Instant registrationDate;

	@Override
	public String toString() {
		return "User [id=" + id + ", username=" + username + ", email=" + email + ", firstname=" + firstname
				+ ", surname=" + surname + ", active=" + active + ", lastLogin=" + lastLogin + ", registrationDate="
				+ registrationDate + "]";
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getSurname() {
		return surname;
	}

	public void setSurname(String surname) {
		this.surname = surname;
	}

	public boolean isActive() {
		return active;
	}

	public void setActive(boolean active) {
		this.active = active;
	}

	public Instant getLastLogin() {
		return lastLogin;
	}

	public void setLastLogin(Instant lastLogin) {
		this.lastLogin = lastLogin;
	}

	public Instant getRegistrationDate() {
		return registrationDate;
	}

	public void setRegistrationDate(Instant registrationDate) {
		this.registrationDate = registrationDate;
	}

}
